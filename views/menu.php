					<p style="line-height:150%;">
						Depuis 1919 date de création de l’établissement historique dit « Colonie de St Fargeau », la fondation Ellen poidatz a renforcé et développé ses actions dans l’accompagnement et le soin des enfants, adolescents et jeunes adultes présentant des déficiences fonctionnelles (motrices, cognitives, intellectuelles, sensorielles) temporaires ou définitives, avec ou sans troubles associés.
						Son activité s’exerce aujourd’hui par la création et la gestion d’établissements sanitaires et médico-sociaux.
						Dans chaque structure, notre volonté est de tout mettre en œuvre pour que votre séjour, bilan, accompagnement, ou prise en charge se déroule dans les meilleures conditions, dans un environnement de compétence, de confort et de sécurité.
						Fondation à but non lucratif régie par la loi de 1901, la Fondation Ellen Poidatz s'est donné pour mission de créer et gérer des établissements et services agréés par le Ministère des Affaires Sociales et de la Santé.
						<br /><br />
						Créée en 1919 par Ellen Poidatz, la future Fondation appelée alors "Colonie de Saint Fargeau" était constituée d'un seul établissement de rééducation ayant pour vocation de soigner et scolariser les enfants démunis atteints par la poliomyélite.
						<br /><br />
						Ce fut le premier centre de ce type créé en France.
						<br /><br />
						Aujourd'hui, au travers  des différentes structures sanitaires ou médico-sociales dont elle assure la gestion, sa vocation est triple :
						Assurer aux  patients les soins et les traitements de rééducation que leur état requiert.
						Pour ceux qui sont en âge scolaire, proposer un enseignement adapté, quelle que soit l'importance de leur handicap grâce à un enseignement modulaire adapté.
						Favoriser l'accès à la meilleure autonomie possible pour  chacun.
						Accueillir, soigner, vivre.
